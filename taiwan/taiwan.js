	var vm = new Vue({
	    el: "#TaiwanPig",
	    data: {
	        filter: "",
	        place_data: place_data
	    },
	    computed: {
	        now_area: function () {
	            var vobj = this;
	            var result = this.place_data.filter(function (obj) {
	                return obj.tag == vobj.filter;
	            });
	            if (result.length == 0) {
	                return null;
	            }
	            return result[0];
	        }
	    }
	});

	$("path").click(function (e) {
		var tagname = $(this).attr("data-name");

		vm.filter = tagname

		var inputurl;
		for(var i = 0; i < place_data.length; i++){
			if(place_data[i].tag == tagname){
				inputurl = place_data[i].website
			}	
		}
		// console.log(inputurl)
		document.getElementById("link").href = inputurl
	});
	
